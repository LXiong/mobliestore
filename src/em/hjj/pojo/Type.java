package em.hjj.pojo;

public class Type {
private int id;
private String name;
private int fid;

public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getFid() {
	return fid;
}
public void setFid(int fid) {
	this.fid = fid;
}

}
